async function run() {
  try {
    const auth = new google.auth.GoogleAuth({
      keyFile: config.CREDENTIALS_PATH,
      scopes: ['https://www.googleapis.com/auth/spreadsheets']
    });
    const sheets = google.sheets({ version: 'v4', auth });

    const meta = await sheets.spreadsheets.get({ spreadsheetId: CONTENT_SHEET_ID });
    const sheet = meta.data.sheets.find(s => s.properties.sheetId === CONTENT_GID);
    const sheetName = sheet.properties.title;

    // Append new rows
    const values = newPosts.map(p => [p.week, p.date, p.brand, p.type, p.topic, p.copy, p.visual, '']);

    await sheets.spreadsheets.values.append({
      spreadsheetId: CONTENT_SHEET_ID,
      range: `${sheetName}!A:H`,
      valueInputOption: 'RAW',
      resource: { values }
    });

    console.log(`Added ${newPosts.length} new post entries to the sheet.`);

  } catch (e) {
    console.error('Error:', e);
    process.exit(1);
  }
}

run();
